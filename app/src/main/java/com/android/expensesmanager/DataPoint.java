package com.android.expensesmanager;

public class DataPoint {
    int xValue;
    int yValue;

    public DataPoint(int xValue, int yValue){
        this.xValue = xValue;
        this.yValue = yValue;
    }

    public int getxvalue() {
        return xValue;
    }

    public int getyValue() {
        return yValue;
    }

}
